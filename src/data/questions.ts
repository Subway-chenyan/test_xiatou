export const calculateResult = (moralScore: number, actionScore: number, gender: string): string => {
  const getMoralLevel = (score: number) => {
    if (score <= -10) return 'low';
    if (score >= 10) return 'high';
    return 'mid';
  };

  const getActionLevel = (score: number) => {
    if (score <= -10) return 'low';
    if (score >= 10) return 'high';
    return 'mid';
  };

  const moralLevel = getMoralLevel(moralScore);
  const actionLevel = getActionLevel(actionScore);

  if (moralLevel === 'low' && actionLevel === 'low') {
    return '乐子人';
  } else if (moralLevel === 'low' && actionLevel === 'mid') {
    return gender === 'male' ? '下头男' : '下头女';
  } else if (moralLevel === 'low' && actionLevel === 'high') {
    return '搅屎棍';
  } else if (moralLevel === 'mid' && actionLevel === 'low') {
    return '观察者';
  } else if (moralLevel === 'mid' && actionLevel === 'mid') {
    return '正常人';
  } else if (moralLevel === 'mid' && actionLevel === 'high') {
    return '大妈';
  } else if (moralLevel === 'high' && actionLevel === 'low') {
    return '局外人';
  } else if (moralLevel === 'high' && actionLevel === 'mid') {
    return '圣母';
  } else if (moralLevel === 'high' && actionLevel === 'high') {
    return '正义伙伴';
  }
  return '正常人';
};

